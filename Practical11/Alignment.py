#define a function to calculate the alignment score
def compare(a,b):
    score = 0#set initial score as zero
    for i in range(len(a)):
        name1 = amino_acid.index(a[i])
        name2 = amino_acid.index(b[i])
        score = score + BLOSUM62[name1][name2]
        #compare the amino acid that in the same position in the two sequences
    print('The alignment score is:',score)
    return score

def calculate_score(a,b):
    edit_distance = 0 #set initial distance as zero
    for i in range(len(a)): #compare each amino acid
        if  a[i]!=b[i]:		
            edit_distance += 1 #add a score 1 if amino acids are different
        percentage = edit_distance/len(a)
    print('The percentage of identical amino acids is:',100*(1-percentage),'%')
    return percentage

